import os
os.environ["TORCHDYNAMO_DISABLE"] = "1"
from transformers import AutoTokenizer, AutoModelForCausalLM
import torch

local_model_path = "./gemma3-finetuned"

tokenizer = AutoTokenizer.from_pretrained(local_model_path)
model = AutoModelForCausalLM.from_pretrained(
    local_model_path,
    torch_dtype=torch.float16 if torch.cuda.is_available() else torch.float32,
    device_map="auto" if torch.cuda.is_available() else None,
    trust_remote_code=True
).eval()

if tokenizer.pad_token is None:
    tokenizer.pad_token = tokenizer.eos_token

device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
print(f"‡πÉ‡∏ä‡πâ‡∏á‡∏≤‡∏ô‡∏ö‡∏ô: {device}")

if not torch.cuda.is_available():
    model.to(device)


def clean_response(text, original_prompt):
    # ‡∏•‡∏ö prompt ‡∏≠‡∏≠‡∏Å‡∏à‡∏≤‡∏Å‡∏ï‡πâ‡∏ô‡∏Ç‡πâ‡∏≠‡∏Ñ‡∏ß‡∏≤‡∏°
    if text.startswith(original_prompt):
        text = text[len(original_prompt):].strip()

    # ‡∏ï‡∏±‡∏î‡∏ó‡∏µ‡πà <|endoftext|> ‡∏ï‡∏±‡∏ß‡∏à‡∏ö‡∏õ‡∏£‡∏∞‡πÇ‡∏¢‡∏Ñ
    if '<|endoftext|>' in text:
        text = text.split('<|endoftext|>')[0].strip()

    return text


def generate_text(prompt, max_tokens=50, temperature=0.7, top_p=0.9):
    try:
        formatted_prompt = f"‡∏Ñ‡∏≥‡∏ñ‡∏≤‡∏°: {prompt}\n‡∏ï‡∏≠‡∏ö: "

        inputs = tokenizer(formatted_prompt, return_tensors="pt", padding=True, truncation=True, max_length=1024)
        inputs = {k: v.to(device) for k, v in inputs.items()}

        with torch.no_grad():
            outputs = model.generate(
                input_ids=inputs['input_ids'],
                attention_mask=inputs.get('attention_mask', None),
                max_new_tokens=max_tokens,
                temperature=temperature,
                top_p=top_p,
                do_sample=True,
                pad_token_id=tokenizer.eos_token_id,
                eos_token_id=tokenizer.eos_token_id,
                repetition_penalty=1.2, 
                num_return_sequences=1,
                early_stopping=True, 
                no_repeat_ngram_size=3, 
            )

        generated_text = tokenizer.decode(outputs[0], skip_special_tokens=True)

        cleaned_text = clean_response(generated_text, formatted_prompt)

        if cleaned_text.startswith("‡∏ï‡∏≠‡∏ö: "):
            cleaned_text = cleaned_text[5:].strip()

        return cleaned_text if cleaned_text else "‡∏Ç‡∏≠‡∏≠‡∏†‡∏±‡∏¢‡∏â‡∏±‡∏ô‡πÑ‡∏°‡πà‡πÄ‡∏Ç‡πâ‡∏≤‡πÉ‡∏à‡∏Ñ‡∏≥‡∏ñ‡∏≤‡∏°"

    except Exception as e:
        return f"‡πÄ‡∏Å‡∏¥‡∏î‡∏Ç‡πâ‡∏≠‡∏ú‡∏¥‡∏î‡∏û‡∏•‡∏≤‡∏î: {str(e)}"


def chat_with_model():
    print("\n" + "=" * 50)
    print("ü§ñ Gemma-3 Chat Bot ‡∏û‡∏£‡πâ‡∏≠‡∏°‡πÉ‡∏ä‡πâ‡∏á‡∏≤‡∏ô!")
    print("=" * 50)

    while True:
        user_input = input("\nüí≠ You: ")

        if not user_input.strip():
            print("‚ùå ‡∏Å‡∏£‡∏∏‡∏ì‡∏≤‡∏û‡∏¥‡∏°‡∏û‡πå‡∏Ç‡πâ‡∏≠‡∏Ñ‡∏ß‡∏≤‡∏°")
            continue

        print("ü§î ‡∏Å‡∏≥‡∏•‡∏±‡∏á‡∏Ñ‡∏¥‡∏î...")
        response = generate_text(
            user_input,
            max_tokens=50,
            temperature=0.8,
            top_p=0.9
        )
        print(f"ü§ñ AI: {response}")


if __name__ == "__main__":
    chat_with_model()